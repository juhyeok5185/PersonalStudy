package Hello.HelloSpring.domain;

public class Member {
    //ID는 시스템이 저장하는 ID를 말한다.
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
