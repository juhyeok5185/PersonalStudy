
public class forExan {

	public static void main(String[] args) {
		
	String str[] = {"하나" , "둘" , "셋"};
		
		for(int i=1; i<3; i++) { // i=0부터 시작해서 3보다 작을때까지 반복 i는 1씩 올라
			System.out.println("Danny");
		}
		System.out.println("for문 끝났당.");
		
		for(int i=0; i < str.length; i++) { //Length(길이)는 배열및 list의 숫자
			System.out.println("인정");
		}

	}

}
